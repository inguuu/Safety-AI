import streamlit as st

def render_examples():
    st.info("""
    💡 **질문 예시:**
    - 야간 근무가 건강에 미치는 영향은?
    - 고혈압 위험요인은 무엇인가요?
    - 건강검진에서 꼭 확인해야 하는 항목은?
    """)

def render_guides():
    with st.expander("❓ 질문 가이드 보기"):
        st.write("""
        - **진료항목**, **검진항목** 대신 **검사항목**으로 질문해보세요.
        - 기관 대신 **센터**라는 단어도 사용 가능합니다.
        - 예: “검진센터의 혈액검사 항목은 무엇인가요?”
        """)
